    public void agruparRadioButtons(){
        btnGroupGenero.add(rbFemenino);
        btnGroupGenero.add(rbMasculino);
    }
    
    public void agruparCheckBoxApoyos(){
        listaApoyos.add(cbDesempleo);
        listaApoyos.add(cb3raEdad);
        listaApoyos.add(cbNini);
        listaApoyos.add(cbSoltero);
    }
   
    public String seleccionarGenero(){
        Enumeration <AbstractButton> btnGroup = btnGroupGenero.getElements(); 
        String genero = ""; 
        while(btnGroup.hasMoreElements()){
            JRadioButton btnAux = (JRadioButton) btnGroup.nextElement(); 
            if(btnAux.isSelected()){
                genero = btnAux.getText(); 
                break; 
            }
        }
        return genero; 
    }
    
    public ArrayList<String> obtenerListaApoyos(){
        ArrayList<String> lista = new ArrayList<>(); 
        for(int i = 0; i < listaApoyos.size(); ++i){
            if(listaApoyos.get(i).isSelected()){
                lista.add(listaApoyos.get(i).getText());
            }
        }
        return lista; 
    }
    
  
    
    public void limpiarCampos(){
        txtNombre.setText("");
        txtCurp.setText("");
        txtRfc.setText("");
        for(JCheckBox aux : listaApoyos){
            aux.setSelected(false);
        }
    }

